          <p>La mayoría de las tradiciones y supersticiones que surgen del hacha suelen aludir a la hoja de esta. La más conocida es una advertencia en cuanto a la manera en que se debe portar el hacha cuando el leñador se <bold>desplaza de una faena a otra; esta</bold> tradición dicta que el leñador jamás debiera apuntar el filo hacia el firmamento, pues se considera una provocación que atrae <em>la mala suerte</em>. Otra tradición señala la manera apropiada de pasar un hacha entre leñadores: la entrega se debe hacer extendiendo el cabo <b>hacia el receptor</b> con el filo apuntando hacia el suelo (por las razones previamente señaladas). Ambas manos deben <i>hacer contacto con la madera; el que entrega</i> debe sujetar el hacha del hombro y no soltar hasta que el receptor se haya aferrado bien de la empuñadura. De lo contrario se tienta la mala suerte y se dice que pueden producirse tensiones anímicas entre los leñadores que participan del intercambio.</p>

