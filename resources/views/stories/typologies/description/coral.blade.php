        <div class="col-sm-7">
          <p>Las narrativas <strong>corales</strong> tienen la característica principal de incluir varios personajes que comparten el papel protagónico del relato. Cada personaje tiene su propia historia y parte de la maestría del autor se evidencia en el modo en que lograr entretejer las distintas líneas narrativas.</p>

          <p>En las visualizaciones de relato coral que propone <strong>Lexia</strong> cada personaje estará representado por un color distintivo. Podés usar un orden definido por vos mismo o dejar que el sistema ordene las historias al azar.</p>

          <div class="figure tam-33">
           <img src="{{ asset('img/home/manhattan.jpg') }}" alt="" class="borde">  
          </div>       

          <p>Las novelas corales forman a veces una especie de mosaico de voces distintas que tienen un objetivo narrativo común. Ese es el caso de <strong>Manahattan Transfer</strong> (1925) de John Dos Passos, considerada la primera novela coral. Ahí los distintos personajes y sus historias son una manera de abordar la dificil tarea de describir la vida en una ciudad en toda su complejidad. Camilo José Cela intenta algo similar para el Madrid y la sociedad española de los años 40 en su monumental <strong>La Colmena</strong> (1951). Algunas series también se proponen retratar mundos y sistemas intrincados que implican la participación de varios ambientes y personajes. Un claro ejemploe es <strong>The Wire</strong> de David Simon y Ed Burns, especialmente en su segunda temporada (2003).</p>

          <div class="figure tam-33">
           <img src="{{ asset('img/home/magnolia.jpg') }}" alt="" class="borde">  
          </div>

          <p>Otras veces la interrelación entre esos personajes es menos global y tiene una dimensión más humana. Es el caso del film <strong>Magnolia</strong> (1999) de Paul Thomas Anderson que presenta varias historias entrecruzadas donde la ciudad de Los Ángeles y el mundo de la televisión funcionan como hilos conectores entre lo que le pasa a una docena de personajes. Otro ejemplo es la película <strong>Short cuts</strong> (1993) de Robert Altman que parte una adaptación de trece cuentos y un poema del escritor norteamericano Raymon Carver.</p>


          <p>Existe un subtipo de relato coral donde las distintas voces construyen perspectivas diferentes sobre uno mismo suceso. Un caso emblemático es el del cuento <strong>En el bosque</strong> (1922) del escritor japonés Ryunosuke Akutagawa, luego llevado al cine por Akira Kurosawa en <strong>Rashomon</strong> (1950). En una trama de corte policial, un detective registra el testimonio de tres personajes distintos que ofrecen interpretaciones contradictorias sobre un crimen.</p>

          <div class="figure tam-33">
            <img src="{{ asset('img/home/barnes.jpg') }}" alt="">  
          </div>

          <p>En la novela <strong>Hablando del asunto</strong> (1991), Julian Barnes elige una estructura similar para describir en tiempo real el desarrollo de un triángulo amoroso entre dos amigos y la esposa de uno de ellos. Los personajes interpelan al lector y le confían sus sentimientos al mismo tiempo que intentan <i>sacarle</i> información sobre lo que los demás personajes dijeron de ellos.</p>

          <p>Frederik Petters experimenta con algo parecido para la historieta en <strong>Constellation</strong> (2003) donde los mismos hechos estan contados desde la perspectiva de un espía que quiere desertar, otro agente en actividad que busca venganza y una inmigrante rusa.</p>


