          <p>Algo por el estilo hizo el escritor uruguayo Mario Levrero en <strong>La novela luminosa</strong> (2005). El autor uruguayo se embarcó en la tarea imposible de escribir una novela donde ciertas experiencias extraordinarias (que él denominaba <i>"luminosas"</i>) debían pasarse al formato novela sin que en el camino pierdan su condición de tales. Más de dos tercios del libro están ordenados en forma de diario y detallan el proceso de escritura y sus dificultades, sus manías, sus procastinaciones, su adicción a las computadoras, su hipocondría, su relación con las mujeres y por sobre toda las cosas el testimonio minucioso de un gran fracaso.</p>

