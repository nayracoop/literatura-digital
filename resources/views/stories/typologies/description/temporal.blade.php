                    <h2>Cómo y cuándo escribir</h2>

            <p>La literatura es una actividad que requiere de dos cosas: estar dispuesto a escribir mucho y leer todavía más. Escribir todos los días es una excelente manera de aprender a soltarse y adquirir experiencia con el manejo del lenguaje.</p>

            <p>Podés probar con sesiones cortas de <strong>no más de 20 o 30 minutos</strong>. Es una medida de tiempo posible y fácil de alternar con otras actividades. Algunos prefieren hacerlo temprano apenas se levantan, otros se sienten más cómodos por la noche antes de ir a acostarse. La brevedad es clave para poder ajustarse a esos momentos menos rutinarios y más ocasionales, como cuando conseguís un asiento en el colectivo.</p>

            <p>Escribir un poco todos los días te va a ayudar a acumular material que te puede ser muy útil para futuros proyectos.</p>

            <div class="figure tam-33">
              <img src="{{ asset('img/home/tweets.jpg') }}" alt="" class="borde">
            </div>


            <p>En 2015 la escritora norteamericana Mira Gonzalez publicó una selección de los tweets que aparecieron en las tres cuentas de Twitter que administró entre 2010 y 2014. El libro se llama <strong>Selected Tweets</strong> (<i>Tweets seleccionados</i>) y también incluye una segunda parte con posteos de otro escritor de alt-lit Tao Lin.</p>

            <h2>Sobre qué escribir</h2>
 
            <p>Una destreza útil y facil de aprender es el de la <strong>escritura libre</strong>. Todo lo que tenés que hacer es hacerte unos minutos y escribir cualquier cosa que te venga a la mente, sin interrupción. Aunque lo que te salgan sean pavadas, no importa, no pares de escribir. Tampoco te pongas a editar lo que ya escribiste, ni vuelvas sobre tus pasos. Si alguien te interrumpe, escribí también esa interrupción. <strong>Lo importante es seguir escribiendo.</strong></p>

            <p>Es posible que al día siguiente o a la media hora pienses que eso que publicaste es una estupidez. No te preocupes, recordá que <strong>Lexia</strong> siempre permite dejar textos en borrador. ¡Y no lo borres! Es posible que eso que escribiste hoy te sea útil en el futuro.</p>
                    
            <p>Como alternativa, podés intentar <strong>describir lo mejor posible</strong> un objeto, un cuadro o la habitación en la que estás. Algunos estilos narrativos como la <a href="https://debedehaber.wordpress.com/2012/01/20/el-nouveau-roman-una-teoria-de-la-novela-moderna/">nouveau roman</a> se apoyan muchísimo en este tipo de escritura.</p>

            <p>También podés ayudarte iniciando tu relato con alguna de las <strong>frases gancho</strong> de esta lista:</p>
                    
            <ul>
             <li>"Me acuerdo la ultima vez que..."</li>
             <li>"Estábamos equivocados..."
             <li>"El dolor era insoportable."</li>
             <li>"Esta vez va a ser diferente."</li>
             <li>"Nuestra única esperanza..."</li>
             <li>"Me di cuenta que había cometido un terrible error cuando..."</li>
             <li>"Tenía gusto a..."</li>
             <li>"Estar equivocado es dificil porque..."</li>
             <li>"Lo odio porque..."</li>
             <li>"Nos hizo rerir porque..."</li>
             <li>"Me arrepiento..."</li>
             <li>"Cuando me desperté eran las 3 de la mañana..."</li>
             <li>"El peor dìa de mi vida fue cuando..."</li>
            </ul>

            <p>Otra opción es contar tu día anterior, una película que viste o relatar un proceso cualquiera a medida que los desarrollás.</p>

