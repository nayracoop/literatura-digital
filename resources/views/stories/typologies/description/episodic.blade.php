        <div class="row">

        <div class="col-sm-7">
            <p>El formato <strong>episódico</strong> se inspira en la forma que tienen algunos relatos de dividir su estructura en una serie de escenas autoconclusivas e independientes que al mismo tiempo mantienen un valor de conjunto.</p>

          <p>Algunos académicos sitúan el origen de este tipo de relatos en las formas narrativas propias de las culturas orales que no conocían la escritura. Determinados géneros populares en estas culturas como la epopeya contaban historias de gran extensión divididas en episodios que rara vez mantenían el orden cronológico de los acontecimientos. Estas narraciones nunca se pasaban al medio escrito y la única forma de conservarlas para luego transmitirlas era por medio de la memoria de los propios relatores que escuchaban y asimilaban las historias que habían escuchado de otros. Así, el tipo de estructura episódica se parece más a la forma en que evocamos un recuerdo.</p>

          <p>Muchas de las primeras novelas conservaron varios rasgos de este tipo de estructura. La figura del héroe errante servía para unir los episodios y se verificá tanto en los romances medievales como en <strong>Don Quijote de la Mancha</strong> (1615) de Cervantes y hasta llega a verificarse algunas obras de Dickens como <strong>Los papeles póstumos del Club Pickwick</strong>  (1837) <a class="citame" href="#cita">[1]</a>. El tipo de historia lineal de orden más o menos cronológico que generalmente encontrás cuando ves una película o leés un libro es de adopción más bien reciente y puede decirse que se consolidó con el relato policial a fines del siglo XIX.</p>


          <p>Algunos autores actuales siguen usando este tipo de estructura para organizar sus obra. <strong>Suicidio</strong> (2007) del francés Edouard Levé está escrita en segunda persona y se presenta como una descripción de las características, actitudes y escenas de un personaje de ficción que acaba de suicidarse. La novela se estructura en párrafos de colocación casi aleatoria y representan la manera en la que recordamos a alguien, según la metáfora del mismo autor un orden parecido al de "elegir bolitas de una bolsa".</p>
  
          <p>Otros escritores llevaron este procedimiento al extremo. En <strong>Composición No.1</strong> (1962), Marc Saporta además de relatar ocho episodios que suceden a lo largo de la vida de un personaje, decide construir un libro sin encuadernación, donde las páginas se ofrecen sueltas para ser mezcladas antes de comenzar el libro. Al pasar cada página el lector va armando los episodios en su cabeza (un accidente de tránsito, una esposa con problemas psiquiátricos, la acción de un grupo de la Resistencia Francesa entre otros) y solo al final logra construir el todo coherente del relato.</p>       

