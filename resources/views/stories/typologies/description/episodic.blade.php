          <p>En un sentido parecido funciona <strong>Pecado</strong> (2008) del ruso Zajar Prilepin. Es una novela en relatos donde en un orden no-cronológico presenciamos distintos momentos en la vida de un mismo personaje: en su niñez, cuando es padre o en sus trabajos como sepulturero, personal de seguridad en una discoteca y soldado en Chechenia.<p>

          <div class="figure tam-33">
            <img src="{{ asset('img/home/perec.jpg') }}" alt="" class="borde">  
          </div>
         
          <p>Otros autores prefieren valerse de metáforas un poco más concretas que funcionan en una manera muy parecida a una novela en episodios. En <strong>La vida instrucciones de uso</strong> (1972)el escritor francés George Perec se vale de la estructura de un edificio parisino para ir alternando las historias del presente y del pasado de cada departamento y sus habitantes.</p>

          <p>En <strong>La maleta</strong> (1983) de Sergei Dovlátov diferentes objetos que salen de una valija (una camisa, unos zapatos o un par de medias) traída por el autor al emigrar de la URSS sucitan una nueva historia.</p>

          <div class="figure tam-33">
            <img src="{{ asset('img/home/calvino.png') }}" alt="" class="borde">  
          </div>

          <p>En <strong>El castillo de los destinos cruzados</strong> (1973) de Italo Calvino las historias que componen el libro parten de unas interpretaciones de las cartas del tarot distintas de las que tradicionalmente se les atribuye. La novela se divide en dos partes: El castillo de los destinos cruzados y La taberna de los destinos cruzados. Dos diseños de naipes distintos (el refinado Visconti y el más tosco Marsella) le sirven a Calvino para contar en el primer libro historias de la nobleza y en el segundo relatos de gente corriente.</p>

          <div class="figure tam-33">
            <img src="{{ asset('img/home/wilson.png') }}" alt="" class="borde">  
          </div>

          <p>Un último grupo de obras tomaron de modelo estructuras un poco más abstractas que también funcionan a la manera de episodios. El libro <strong>Leñador</strong> (2013) de Mike Wilson presenta una serie de continua de términos relacionados con la vida de los leñadores como <i>hacha</i>, <i>trenzador</i> o <i>cerveza</i>. La explicación de cada palabra se desarrolla con un registro similar al de las enciclopedias. La suma de todos estos conceptos desarrollados a lo largo de más de 400 páginas logra condensar la riqueza de la vida en el bosque.</p>

          <p>En el <strong>Diccionario Jázaro</strong> (1982) la estructura propia de un diccionario le permite a su autor Milorad Pávic ordenar la historia ficticia del pueblo jázaro. En la ficción del libro, los jázaros habrían recibido la influencia de algunas de las tres religiones monoteistas más importantes: la católica, el judaísmo o el islam. Pavic incluye un diccionario distinto para cada culto que explica por medio de entradas de diccionario la historia de determinados mitos y personajes que pertenecieron a ese pueblo. A través de los términos comunes entre las tres partes se figura la influencia cultural de cada una de esa religiones para explicar la historia.</p>


